// ---------------------------------------------------------- Data Type -------------------------------------------------------

//   -------String Type-------
let name = "Payal Sharma";
console.log(name)
console.log(typeof name)   

//   -------Number Type-------
let age = 0.21
console.log(age)
console.log(typeof age)  

//   -------Boolean Type-------
let logged_out = true
console.log(logged_out)
console.log(typeof logged_out) 


//   -------null Type-------
let value = null
console.log(value)
console.log(typeof value) 

//   -------undefined Type-------
let value_defined = undefined
console.log(value_defined)
console.log(typeof value_defined) 


